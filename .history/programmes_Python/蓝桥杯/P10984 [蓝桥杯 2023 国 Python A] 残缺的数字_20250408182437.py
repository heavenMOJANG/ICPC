tab = [0b1111110, 0b0110000, 0b1101101, 0b1111011, 0b0110111, 0b1110011, 0b0111111, 0b1110000, 0b1111010, 0b1110111]
inp = [0b0000011, 0b1001011, 0b0000001, 0b0100001, 0b0101011, 0b0110110, 0b1111111, 0b0010110, 0b0101001, 0b0010110, 0b1011100, 0b0100110, 0b1010000, 0b0010011, 0b0001111, 0b0101101, 0b0110101, 0b1101010]
ans = 1
for x in inp:
    cnt = 0
    for y in tab:
        if x & y != 0:
            cnt += 1
    ans *= cnt
print(ans)