#include<bits/stdc++.h>
typedef long long ll;
using namespace std;
constexpr ll inf=0x7fffffff;
void solve(){
    ll a,b,n;cin>>a>>b>>n;
    ll res=b;
    for(ll i=1,x;i<=n;++i){
        cin>>x;
        res+=min(1+x,a)-1;
    }
    cout<<res<<"\n";
    return;
}
